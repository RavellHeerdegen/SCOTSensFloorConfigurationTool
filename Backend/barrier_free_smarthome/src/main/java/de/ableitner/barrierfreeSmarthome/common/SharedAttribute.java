package de.ableitner.barrierfreeSmarthome.common;

public class SharedAttribute {
	
	// ============================================================================================================================================
	// ============================================================================================================================================
	// Attributes
	// ============================================================================================================================================
	// ============================================================================================================================================
	
	private String key;

	private String type;

	private Object value;
	
	
	
	
	// ============================================================================================================================================
	// ============================================================================================================================================
	// Constructors
	// ============================================================================================================================================
	// ============================================================================================================================================
	
	public SharedAttribute(String key, String type, Object value){
		this.setKey(key);
		this.setType(type);
		this.setValue(value);
	}
	
	
	
	
	// ============================================================================================================================================
	// ============================================================================================================================================
	// Getters and Setters
	// ============================================================================================================================================
	// ============================================================================================================================================
	
	public String getKey() {
		return this.key;
	}

	private void setKey(String key) {
		this.key = key;
	}

	public String getType() {
		return this.type;
	}

	private void setType(String type) {
		this.type = type;
	}

	public Object getValue() {
		return this.value;
	}

	private void setValue(Object value) {
		this.value = value;
	}
	
	
	
	
	// ============================================================================================================================================
	// ============================================================================================================================================
	// override Methods
	// ============================================================================================================================================
	// ============================================================================================================================================
	
	@Override
	public String toString(){
		String result = "SharedAttribute:   key=" + this.key;
		result += "   type=" + this.type;
		result += "   value=" + this.value.toString();
		return result;
	}
	
	
	
	
	// ============================================================================================================================================
	// ============================================================================================================================================
	// public Methods
	// ============================================================================================================================================
	// ============================================================================================================================================
	
	// ============================================================================================================================================
	// ============================================================================================================================================
	// private Methods
	// ============================================================================================================================================
	// ============================================================================================================================================
		
}
